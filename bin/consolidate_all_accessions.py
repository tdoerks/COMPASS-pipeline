def main():
    base_dir = os.path.expanduser('~/compass_kansas_results')
    output_dir = os.path.expanduser('~/compass_kansas_results/publication_analysis/accessions')

    Path(output_dir).mkdir(parents=True, exist_ok=True)

    print("=" * 70)
    print("Kansas E. coli Accession Consolidation")
    print("=" * 70)

    # Load all accessions
    accessions = load_all_accessions(base_dir)

    if not accessions:
        print("\n❌ No accessions found!")
        return

    # Calculate stats
    stats = generate_summary_stats(accessions)

    # Save outputs
    save_master_accession_list(accessions, f"{output_dir}/kansas_all_accessions_full.csv")
    save_accession_list_only(accessions, f"{output_dir}/kansas_all_accessions.txt")
    save_by_year_breakdown(accessions, f"{output_dir}/by_year")
    generate_sra_download_script(accessions, f"{output_dir}/download_all_sra.sh")
    generate_summary_report(accessions, stats, f"{output_dir}/ACCESSION_SUMMARY.html")

    print("\n" + "=" * 70)
    print("✅ Consolidation complete!")
    print("=" * 70)
    print(f"\nOutput directory: {output_dir}")
    print(f"\nFiles created:")
    print(f"  - kansas_all_accessions_full.csv (complete metadata)")
    print(f"  - kansas_all_accessions.txt (simple list)")
    print(f"  - by_year/ (separate files per year)")
    print(f"  - download_all_sra.sh (download script)")
    print(f"  - ACCESSION_SUMMARY.html (summary report)")
    print("=" * 70)


if __name__ == '__main__':
    main()
