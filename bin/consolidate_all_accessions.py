#!/usr/bin/env python3
"""
Consolidate All SRA Accessions from Kansas E. coli Project

Pulls together all SRR accessions from all year directories into:
1. Master accession list (CSV)
2. Year-by-year breakdown
3. Metadata summary
4. SRA download commands

Useful for:
- Re-downloading data
- Sharing accession list for publication
- Creating supplementary tables
- Tracking which samples were processed
"""

import csv
import os
from pathlib import Path
from collections import defaultdict


def load_all_accessions(base_dir):
    """Load all accessions from all year directories"""
    print("Loading accessions from all years...")

    all_accessions = []
    base_path = Path(base_dir)

    for year in [2021, 2022, 2023, 2024, 2025]:
        metadata_file = base_path / f"results_kansas_{year}" / "filtered_samples" / "filtered_samples.csv"

        if not metadata_file.exists():
            print(f"  ⚠️  {year}: No filtered_samples.csv found")
            continue

        year_count = 0
        with open(metadata_file) as f:
            reader = csv.DictReader(f)
            for row in reader:
                accession = {
                    'srr_accession': row.get('Run', ''),
                    'sample_name': row.get('SampleName', ''),
                    'year': year,
                    'organism': row.get('organism', ''),
                    'biosample': row.get('BioSample', ''),
                    'bioproject': row.get('BioProject', ''),
                    'library_strategy': row.get('LibraryStrategy', ''),
                    'platform': row.get('Platform', ''),
                    'instrument': row.get('Instrument', ''),
                    'bases': row.get('Bases', ''),
                    'avg_length': row.get('AvgSpotLen', ''),
                    'collection_date': row.get('Collection_Date', ''),
                    'geo_loc': row.get('geo_loc_name', ''),
                    'isolation_source': row.get('Isolation_Source', '')
                }
                all_accessions.append(accession)
                year_count += 1

        print(f"  ✅ {year}: {year_count} accessions")

    print(f"\n  Total: {len(all_accessions)} accessions")
    return all_accessions


def generate_summary_stats(accessions):
    """Calculate summary statistics"""
    print("\nCalculating summary statistics...")

    stats = {
        'total': len(accessions),
        'by_year': defaultdict(int),
        'by_organism': defaultdict(int),
        'by_platform': defaultdict(int),
        'bioprojects': set(),
        'unique_samples': set()
    }

    for acc in accessions:
        stats['by_year'][acc['year']] += 1
        stats['by_organism'][acc['organism']] += 1
        stats['by_platform'][acc['platform']] += 1
        if acc['bioproject']:
            stats['bioprojects'].add(acc['bioproject'])
        if acc['sample_name']:
            stats['unique_samples'].add(acc['sample_name'])

    return stats


def save_master_accession_list(accessions, output_file):
    """Save complete accession list to CSV"""
    print(f"\nSaving master accession list: {output_file}")

    with open(output_file, 'w', newline='') as f:
        fieldnames = ['srr_accession', 'sample_name', 'year', 'organism',
                     'biosample', 'bioproject', 'library_strategy', 'platform',
                     'instrument', 'bases', 'avg_length', 'collection_date',
                     'geo_loc', 'isolation_source']

        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(accessions)

    print(f"  ✅ Saved {len(accessions)} accessions")


def save_accession_list_only(accessions, output_file):
    """Save simple list of just SRR accessions (one per line)"""
    print(f"\nSaving simple accession list: {output_file}")

    with open(output_file, 'w') as f:
        for acc in accessions:
            f.write(f"{acc['srr_accession']}\n")

    print(f"  ✅ Saved {len(accessions)} accessions")


def save_by_year_breakdown(accessions, output_dir):
    """Save separate accession lists by year"""
    print(f"\nSaving year-by-year breakdown...")

    Path(output_dir).mkdir(parents=True, exist_ok=True)

    by_year = defaultdict(list)
    for acc in accessions:
        by_year[acc['year']].append(acc)

    for year in sorted(by_year.keys()):
        output_file = os.path.join(output_dir, f"accessions_{year}.txt")
        with open(output_file, 'w') as f:
            for acc in by_year[year]:
                f.write(f"{acc['srr_accession']}\n")
        print(f"  ✅ {year}: {len(by_year[year])} accessions → {output_file}")


def generate_sra_download_script(accessions, output_file):
    """Generate bash script to download all SRA data"""
    print(f"\nGenerating SRA download script: {output_file}")

    script = """#!/bin/bash
# Download all Kansas E. coli SRA data
# Generated by consolidate_all_accessions.py

set -e

OUTDIR="sra_downloads"
mkdir -p $OUTDIR

echo "Downloading SRA data for {total} accessions..."
echo "This may take several hours/days depending on data size"
echo ""

# Check if prefetch is installed
if ! command -v prefetch &> /dev/null; then
    echo "ERROR: prefetch (SRA Toolkit) not found!"
    echo "Install with: conda install -c bioconda sra-tools"
    exit 1
fi

# Download each accession
""".format(total=len(accessions))

    for i, acc in enumerate(accessions, 1):
        srr = acc['srr_accession']
        script += f"""
# {i}/{len(accessions)}: {srr} ({acc['year']}, {acc['sample_name']})
echo "Downloading {srr}..."
prefetch {srr} -O $OUTDIR
"""

    script += """
echo ""
echo "✅ Download complete!"
echo "Total accessions: {total}"
echo "Output directory: $OUTDIR"
""".format(total=len(accessions))

    with open(output_file, 'w') as f:
        f.write(script)

    # Make executable
    os.chmod(output_file, 0o755)

    print(f"  ✅ Saved: {output_file}")


